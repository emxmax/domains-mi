<h1>¿Quiénes somos?</h1>

<p>
	Somos un medio especializado en la difusión de noticias 
	relevantes para el sector minero, a nivel nacional e internacional.
	 Buscamos no solo llevar información, sino mantenerlos actualizados,
	  en tiempo real, a través de temas de interés social, tecnología e innovación minera. 
	  Esto con el objetivo de crear una comunidad interesada en un crecimiento minero responsable y seguro.

</p>