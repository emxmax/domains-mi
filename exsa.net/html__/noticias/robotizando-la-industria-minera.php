					<div class="text-noticia">

						<p>Se calcula que, en 10 años, el llamado “empleado del futuro” tendrá bajo su responsabilidad el manejo de las relaciones con las comunidades aledañas a los proyectos mineros.</p>

						<p>De acuerdo con <a href="http://peru.angloamerican.com/?sc_lang=es-es">Anglo American</a>, algunas de las minas serán manejadas casi completamente sin la intervención humana en algunos años. Es decir, que dependerán de una variedad de sensores y modelos virtuales para la realización de tareas.</p>

						<p>Fue durante una conferencia en Londres, que Tony O’Neill, Director de Tecnologías de la gigante minera, destacó que están apostando por la tecnología en miras de aumentar la eficiencia en los procedimientos productivos. A consecuencia de ello, se contará con una reducción de costos y un menor impacto en el medio ambiente.</p>

						<p>Vale recalcar que, Anglo American opera algunos de los depósitos de cobre más valiosos a nivel mundial y cuenta con una fuerza de trabajo de 87,000 trabajadores de Sudáfrica a Chile, donde maneja junto a <a href="https://www.codelco.com/">Codelco</a> la Minera Andina.</p>

						<p>Los cambios en la industria son a corto plazo, entre 5 o 6 años en el futuro, debido a que cada vez se requiere menos infraestructuras en comparación a los métodos actuales de explotación, comercialmente esto resulta muy atractivo y acelera los cambios.</p>

						<p>De igual manera, otras empresas grandes del sector, se encuentran recorriendo el mismo rumbo de la tecnología hacia la actualización. Un ejemplo de ello es <a href="https://www.bhp.com/espanol">BHP Billiton Ltd</a>. que ha empezado a trabajar para implementar trenes autónomos a lo largo de su red ferroviaria de 1.300 kilómetros. Otro caso similar es el de <a href="https://barricklatam.com/peru/">Barrick Gold Corp. que</a> está a pocos meses del ensayo más ambicioso de la industria minera de oro para modernizar la excavación, usando miles de sensores en la mina Cortez en Nevada.</p>

