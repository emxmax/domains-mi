					<div class="text-noticia">

						<p>Para este tipo de minería se necesita construir túneles, pozos, chimeneas, galerías y otras infraestructuras que permitan extraer el mineral alojado debajo de la superficie del terreno.</p>

						<p>Esta forma de explotación de yacimientos subterráneos se desarrolla, generalmente, debido a que las condiciones sociales o ambientales no permiten que se realice a tajo abierto. Por esta razón, es necesario usar diferentes equipos, instrumentos y tecnologías para iniciar las operaciones.</p>

						<p>En ese sentido, un punto fundamental para la minería subterránea es la ventilación, ya que requiere sistemas especiales para generar oxígeno, reducir temperaturas, diluir y remover polvos y gases nocivos. Esta es la única forma de garantizar un ambiente laboral seguro y confiable.</p>

						<p>Otro aspecto esencial es la construcción de diversas infraestructuras que ayuden al desarrollo de la actividad. Algunos de los métodos más empleados son los de túneles y pilares, hundimientos, corte y relleno (<em>Cut and fill mining</em>), realce por subniveles (<em>Sublevel stopping</em>) y cámaras-almacén (<em>Shrinkage</em>).</p>

						<p>De acuerdo a las estadísticas del <a href="http://www.minem.gob.pe/">Ministerio de Energía y Minas (MEM)</a>, hasta el año 2011, en el Perú se encontraban registradas 190 concesionarias mineras subterráneas. Con respecto a su ubicación, la mayoría se encontraban en La Libertad (50), Arequipa (33) y Lima (23).</p>

						<p>De igual manera, hasta ese entonces, solamente el 38% de esas unidades en operación usaban el método de explotación subterránea, destacando así las minas polimetálicas de Milpo, Atacocha, Volcan, Cobriza, Morococha, Casapalca y Santa Luisa; las minas de estaño de San Rafael y San Vicente; y las minas de oro Marca, Horizonte, Poderosa, Orcopampa, entre otras.</p>

						<p>Hoy en día, la minería subterránea en nuestro país tiene mayores exigencias que han sido cumplidas gracias a los avances tecnológicos. Cada día más empresas locales y extranjeras vienen apostando por innovaciones que permitan mejorar la productividad de sus unidades para convertirse en aliados estratégicos de las mineras.</p>

